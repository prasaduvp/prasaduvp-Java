package OperatorPractice;

public class ConditionalLogicalOperators {
	
	public static void main(String[] args) {
		
		boolean j = true;
		boolean p = false;
		
		System.err.println(j&&p);
		System.err.println(j||p);
		System.err.println(!j);
		System.err.println(!p);
		System.out.println(p&&j);
		System.out.println(p||j);
	}

}
