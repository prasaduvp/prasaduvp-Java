package OperatorPractice;

public class Arithmetic {
		
		public static void main(String[] args) {
			
			 int i = 20;
			 int j = 50;
			 
			System.err.println(i+j);
			System.err.println(i-j);
			System.err.println(i*j);
			System.err.println(i/j);
			System.err.println(i%j);
		}
		
}
