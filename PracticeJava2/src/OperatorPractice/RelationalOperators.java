package OperatorPractice;

public class RelationalOperators {
	public static void main(String[] args) {
		int j = 30;
		int p = 50;
		
		System.err.println(j==p);
		System.err.println(j!=p);
		System.err.println(j>p);
		System.err.println(j<p);
		System.err.println(j>=p);
		System.err.println(j<=p);
	}
}
